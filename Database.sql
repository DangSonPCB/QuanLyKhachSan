-- 1. Bảng PHONG (H1)
CREATE TABLE PHONG (
    SoPhong VARCHAR(10) PRIMARY KEY,
    Loai NVARCHAR(50),
    GiaThue MONEY,
    TrangThaiThue NVARCHAR(50)
);

-- 2. Bảng KHACHHANG (H3)
CREATE TABLE KHACHHANG (
    MaKH VARCHAR(10) PRIMARY KEY,
    TenKH NVARCHAR(100),
    SDTKH VARCHAR(15),
    NgaySinhKH DATE,
    GioiTinh NVARCHAR(10),
    CCCDKH VARCHAR(20),
    emailKH VARCHAR(100)
);

-- 3. Bảng DICHVU (H5)
CREATE TABLE DICHVU (
    MaDV VARCHAR(10) PRIMARY KEY,
    TenDV NVARCHAR(100),
    GiaDV MONEY,
    BatDau TIME,
    KetThuc TIME
);

-- 4. Bảng NHANVIEN (H10)
CREATE TABLE NHANVIEN (
    MaNV VARCHAR(10) PRIMARY KEY,
    TenNV NVARCHAR(100),
    NgaySinhNV DATE,
    ChucVu NVARCHAR(50),
    SDTNV VARCHAR(15),
    CCCDNV VARCHAR(20)
);

-- 5. Bảng DATPHONG (H2)
CREATE TABLE DATPHONG (
    MaDatPhong VARCHAR(10) PRIMARY KEY,
    SoPhong VARCHAR(10) FOREIGN KEY REFERENCES PHONG(SoPhong),
    MaKH VARCHAR(10) FOREIGN KEY REFERENCES KHACHHANG(MaKH),
    NgayNhan DATE,
    NgayTra DATE,
    SoKhanhhang INT
);

-- 6. Bảng SUDUNG_DV (H4)
CREATE TABLE SUDUNG_DV (
    MaDV VARCHAR(10) FOREIGN KEY REFERENCES DICHVU(MaDV),
    MaKH VARCHAR(10) FOREIGN KEY REFERENCES KHACHHANG(MaKH),
    NgaySuDung DATE,
    SoLuong INT,
    PRIMARY KEY (MaDV, MaKH, NgaySuDung)
);

-- 7. Bảng DANHGIA (H6)
CREATE TABLE DANHGIA (
    MaDG VARCHAR(10) PRIMARY KEY,
    MaKH VARCHAR(10) FOREIGN KEY REFERENCES KHACHHANG(MaKH),
    DiemDG INT,
    NoiDungDG NVARCHAR(MAX),
    NgayDG DATE
);

-- 8. Bảng LUONG (H7)
CREATE TABLE LUONG (
    MaLuong VARCHAR(10) PRIMARY KEY,
    MaNV VARCHAR(10) FOREIGN KEY REFERENCES NHANVIEN(MaNV),
    ThanhToan MONEY
);

-- 9. Bảng HOADON (H8)
CREATE TABLE HOADON (
    MaHD VARCHAR(10) PRIMARY KEY,
    MaNV VARCHAR(10) FOREIGN KEY REFERENCES NHANVIEN(MaNV),
    MaKH VARCHAR(10) FOREIGN KEY REFERENCES KHACHHANG(MaKH),
    NgayIn DATE
);

-- 10. Bảng TAIKHOAN (H9)
CREATE TABLE TAIKHOAN (
    ID VARCHAR(10) PRIMARY KEY,
    MaNV VARCHAR(10) NULL,
    MaKH VARCHAR(10) NULL,
    TenDangNhap VARCHAR(50),
    MatKhau VARCHAR(50),
    TrangThai NVARCHAR(50),

    FOREIGN KEY (MaNV) REFERENCES NHANVIEN(MaNV),
    FOREIGN KEY (MaKH) REFERENCES KHACHHANG(MaKH),

    CHECK (
        (MaNV IS NOT NULL AND MaKH IS NULL) OR
        (MaNV IS NULL AND MaKH IS NOT NULL)
    )
);

-- 11. Bảng PHANCONG_NV (H11)
CREATE TABLE PHANCONG_NV (
    MaNV VARCHAR(10) FOREIGN KEY REFERENCES NHANVIEN(MaNV),
    SoPhong VARCHAR(10) FOREIGN KEY REFERENCES PHONG(SoPhong),
    CaLamViec NVARCHAR(50),
    NgayPhanCong DATE,
    PRIMARY KEY (MaNV, SoPhong, NgayPhanCong)
);

-- 12. Bảng CHITIET_HD_DV (H12)
CREATE TABLE CHITIET_HD_DV (
    MaHD VARCHAR(10) FOREIGN KEY REFERENCES HOADON(MaHD),
    MaDV VARCHAR(10) FOREIGN KEY REFERENCES DICHVU(MaDV),
    SoLuong INT,
    PRIMARY KEY (MaHD, MaDV)
);
GO

